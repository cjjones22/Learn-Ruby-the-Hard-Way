x = "There are #{10} types of people." # sets variable x equal to number #{10} within a string.
binary = "binary" # sets variable binary equal to string. 
do_not = "don't" # sets variable do_not equal to string.
y = "Those who know #{binary} and those who #{do_not}." # sets y to variable #{binary} and variable #{do_not}
# within a string.

puts x # outputs variable x.
puts y # outputs variable y.

puts "I said: #{x}." # outputs variable #{x} within a string.
puts "I also said: '#{y}'." # outputs variable #{y} within a string.

hilarious = false # sets variable hilarious equal to a boolean value.
joke_evaluation = "Isn't that joke so funny?! #{hilarious}" # sets variable joke_evaluation to string.

puts joke_evaluation # outputs variable joke_evaluation

